
#include <iostream>
using namespace std;
int main()
{
  // Declearing variables
  double num1, num2;

  // Take a Input
  cout << " Enter Two Integer ";
  cin >> num1 >> num2;

  cout << num1 << " + " << num2 << " = " << num1 + num2 << endl;

  cout << num1 << " - " << num2 << " = " << num1 - num2 << endl;

  cout << num1 << " * " << num2 << " = " << num1 * num2 << endl;

  cout << num1 << " / " << num2 << " = " << num1 / num2 << endl;

  return 0;
}